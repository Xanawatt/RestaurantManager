package Menu;
/**
 *
 * @author garre
 */
public class MenuItem {
    
    int itemID;
    String itemName;
    double price;
    int calories;    
    
    public int getItemID() {
        return itemID;
    }

    public String getItemName() {
        return itemName;
    }

    public double getPrice() {
        return price;
    }
    
    public int getCalories() {
        return calories;
    }
    
}
